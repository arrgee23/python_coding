import numpy as np

class Matrix:

    def __init__(self,mat):
     
        self.row = mat.shape[0]
        self.col = mat.shape[1]
        self.mat = mat

def window_multiply(matrix,kernel,i,j):
    sum = 0
    for k in range(kernel.row):
        for l in range(kernel.col):
            sum+=matrix.mat[i+k][j+l]*kernel.mat[k][l]

    return sum

def convolution(matrix,kernel):
    newrow = matrix.row-kernel.row + 1
    newcol = matrix.col-kernel.col + 1
    result = np.zeros((newrow, newcol))

    for i in range(newrow):
        for j in range(newcol):
            result[i][j] = window_multiply(matrix,kernel,i,j)

    return result


